// Escape Sequences

//  \n - New Line Escape Sequence
console.log("Hello \n Natalie!"); 
//  \n - Tab Escape Sequence
console.log("Hello \t Natalie!"); 

// Back Slash Escape Sequence
console.log('Hello \'Natalie\' Again!')

console.log('\\n');


// String Concatenation

userName = "Natalie!"
regID = 1234566;
console.log("Hello " + userName + " your registration ID is " + regID);

// Template Literal
console.log(`Hello ${userName} your registration ID is ${regID}`);




