// Math Object

// Square Root Of A Number

console.log(Math.sqrt(64));
console.log(Math.sqrt(59).toFixed(3));


// Absolute Value (Mod Value)

console.log(Math.abs(-126));


// Floor And Ceil - 
// Floor value depreciates a fractional value to its previous integral value
// Ceil Value Elevates a fractional value to its next integral value

console.log(Math.floor(4.2));
console.log(Math.ceil(4.2));




// Power - A Number Raised to a power

console.log(Math.pow(3.5, 3));
console.log(Math.pow(-5, 3));
console.log(Math.pow(-5, 2));

// Max And Min Functions - Helps us to find maximum and minmum

console.log(Math.max(1, 5));
console.log(Math.max(1, 5, 6, 7));

console.log(Math.min(-1, 2, -4, 1, 5));


// Logarithmic Functions
// Base2 Log
console.log(Math.log2(8));
// Base 10 Log 
console.log(Math.log10(100));










